#python casting
# casting digunakan untuk menentukan tipe data tertentu sebuah variabel. 
#contoh:
#integer
a = int(3) # a akan menjadi 3 bertipe int
b = int(3.6) # b akan menjadi 3 bertipe int
c = int ("5") # c akan menjadi 5 bertipe int

# float
a = float(3) # a akan menjadi 3.0 bertipe float
b = float(3.6) # b akan menjadi 3.6 bertipe float
c = float ("5") # c akan menjadi 5.0 bertipe float

# string
a = str(3) # a akan menjadi '3' bertipe str
b = str(3.6) # b akan menjadi '3.6' bertipe str
c = str ("5") # c akan menjadi '5' bertipe str
